puts "What's your favorite car marque?"
marque = gets.chomp

case marque
when "Mercedes"
  puts "The best or nothing!"
when "Ford"
  puts "The car you always promised yourself."
when "Jaguar"
  puts "Grace, space and pace."
when "Honda"
  puts "The power of dreams."
when  "Aston Martin"
  puts "Power, Beauty and Soul."
else
  puts "I don't know that marque!"
end
